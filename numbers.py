# importing math functions
from math import*

# floor functions round down from 3.7 to 3
print(floor(3.7))

# ceil functions,n round numbers up
print(ceil(3.7))

# square root function
print(sqrt(36))

# positive and negative numbers
print(2.0987)

# example of addition
print(3 + 4.5)

# printing with variable / converting numbers to string
my_num = 5
print(str(my_num))

# absolute value
my_num = -5
print(abs(my_num))

# power value, 5 raised to the power of 2
print(pow(5, 2))

# max function, returning a larger number which is 6
print(max(4, 6))

# min function, returning a smaller number which is 4
print(max(4, 6))

# round function, 3.7 --> 4
print(round(3.7))


